public class Book {
	
	//Create variable for book
	String title;
	String author;
	int pages;
	String language;

	public Book(String title, String author, int pages, String langague) {
		
		this.title = title;
		this.author = author;
		this.pages = pages;
		this.language = langague;
		
	}
}
